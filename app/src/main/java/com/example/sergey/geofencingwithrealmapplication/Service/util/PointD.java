package com.example.sergey.geofencingwithrealmapplication.Service.util;

public class PointD {

    private double x;
    private double y;

    public PointD(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
